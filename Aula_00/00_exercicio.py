# 2. Faça um algoritmo que leia três valores em variáveis distintas e armazene a soma em outra
# variável. Escreva o valor da soma.

valor_um = int(input("Valor um!"))
valor_dois = int(input("Valor dois!"))
valor_tres = int(input("Valor três!"))

soma = valor_um + valor_dois + valor_tres

print("A soma foi ", soma)
